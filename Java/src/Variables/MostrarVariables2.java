/*Como en el ejemplo anterior  pero ahora vamos agregar una variable  
 con otra variable, de igual manera se usa 
 el signo +*/
package Variables;

public class MostrarVariables2 {
	public static void main(String[] args) {
		String nombre = "Saul ";
		String apellido = "Hidalgo";
		String nombreCompleto = nombre + apellido;
		System.out.println(nombreCompleto);
		/*
		 * Se creo una nueva varibale para guardar los datos de la primera y segunda
		 * variable
		 */

		// Ejemplo en numeros

		int x = 50;
		int y = 50;
		int suma = x + y;
		System.out.println(suma);

	}

}
