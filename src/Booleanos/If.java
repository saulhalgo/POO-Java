//Utilice  "if" para especificar un bloque de código en Java que se ejecutará si una condición es true
package Booleanos;

public class If {
	public static void main(String[] args) {
		if (20 > 18) {
			System.out.println("\"20\" es mayor a \"18\"");
		}

		int x = 50;
		int y = 35;
		if (x > y) {
			System.out.println("x es mayor a y ");
		}

	}

}
