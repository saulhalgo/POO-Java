//Imprimir los elementos de una matriz o recorrer mediante un ciclo for 
package Matrices;

public class Bucles_Matrices_for {
	public static void main(String[] args) {

		String[] carros = { "Volvo", "BMW", "Ford", "Mazda" };

		for (int i = 0; i < carros.length; i++) {
			System.out.println(carros[i]);
			// imprime cada uno de los elemnetos que tiene la matriz
		}

	}

}
