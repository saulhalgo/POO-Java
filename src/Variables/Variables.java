//Existen diferentes tipos de variables para almacenar datos 
package Variables;

public class Variables {
	public static void main(String[] args) {
		String nombre = "Saul";
		int edad = 22;
		float estatura = 1.65f;
		char inicial = 'S';
		boolean hombre = true;
//Para crear una variable se tiene que poner el tipo, nombre de la variable  y darle un valor
		System.out.println(nombre);
		System.out.println(edad);
		System.out.println(estatura);
		System.out.println(inicial);
		System.out.println(hombre);
//Para que reconozca el dato float poner una f al final del valor 
	}
}