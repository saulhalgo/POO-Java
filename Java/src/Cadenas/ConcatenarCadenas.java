//El operador "+" se puede utilizar entre cadenas para combinarlas. Esto se llama concatenación
package Cadenas;

public class ConcatenarCadenas {
	public static void main(String[] args) {
		String firtsName = "Saul";
		String lastName = "Hidalgo";
		System.out.println(firtsName + " " + lastName);
		// primer nombre + espacio + apellido, las comillas dobles se implementa el  espacio
	}

}
