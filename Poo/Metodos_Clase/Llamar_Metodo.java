//Recordemos que un metodo se crea dentro de una clase 
package Metodos_Clase;

public class Llamar_Metodo {
	// Repasaremos como crear un metodo y como llamarlo
	static void miMetodo() { //-----> Si no queremos refresar un valor se pone " void " 
		System.out.println("Hola Mundo");
	}
	public static void main(String[] args) {
		miMetodo();
	}
}
